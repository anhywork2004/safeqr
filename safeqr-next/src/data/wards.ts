export function getWardById(id: string): WardInfo | undefined {
  return WARDS.find((w) => w.id === id);
}

export function searchWards(query: string): WardInfo[] {
  const q = query.toLowerCase().trim();
  if (!q) return WARDS;
  return WARDS.filter(
    (w) =>
      w.name.toLowerCase().includes(q) ||
      w.id.toLowerCase().includes(q)
  );
}
